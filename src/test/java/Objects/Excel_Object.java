package Objects;

public class Excel_Object {
    private String TC_ID;
    private String TC_Summary;
    private String TC_Result;
    private String TC_Note;

    public String getTC_ID() {
        return TC_ID;
    }

    public void setTC_ID(String TC_ID) {
        this.TC_ID = TC_ID;
    }

    public String getTC_Summary() {
        return TC_Summary;
    }

    public void setTC_Summary(String TC_Summary) {
        this.TC_Summary = TC_Summary;
    }

    public String getTC_Result() {
        return TC_Result;
    }

    public void setTC_Result(String TC_Result) {
        this.TC_Result = TC_Result;
    }

    public String getTC_Note() {
        return TC_Note;
    }

    public void setTC_Note(String TC_Note) {
        this.TC_Note = TC_Note;
    }
}
